"""GPU GFP-masking example on a public S3 multiscale OME-Zarr.

Reads a SmartSPIM/HCR fused volume from S3 at a chosen multiscale level, runs the
``gfp-mask`` model (a cupy/cuCIM masking routine driven by the inference pipeline),
and writes a ``uint8`` mask back to S3 as an OME-Zarr that mirrors the source pyramid.

The segmented level **L is parsed from the trailing level digit of ``--in-path``**
(e.g. ``.../fused.zarr/5`` -> L=5). The mask is written at that same level index, and
the coarser levels the source has below L (``L+1 .. N``) are regenerated by
downsampling the mask with the ``max`` (any-positive) reducer, which keeps every level
a strict binary mask. The output's OME-NGFF metadata copies the source datasets
``L..N`` verbatim (same paths and scales), so the mask overlays the original.

Requirements
------------
- A CUDA GPU with the post-processing stack installed:
  ``pip install -e ".[postprocess]"`` (``cupy-cuda12x`` + ``cucim-cu12``). The
  ``gfp-mask`` model is GPU-only.
- AWS credentials with write access to the output bucket. The input bucket
  ``aind-open-data`` is public (region ``us-west-2``); reading uses the standard AWS
  credential chain. If your environment has no credentials and signed S3 reads fail,
  TensorStore's s3 driver may need anonymous access configured.
- Run from the ``examples/`` directory (or with it on ``PYTHONPATH``) so
  ``example_utils`` is importable.

Usage
-----
python examples/run_gfp_mask_example.py \
    --in-spec in_spec.json \
    --out-bucket my-output-bucket \
    --out-prefix predictions/HCR_831990 \
    --devices cuda:0

``--in-spec`` is a TensorStore JSON spec (file path, raw JSON, or dict); the level L is
read from the trailing digit of its kvstore path. Omitting ``--in-spec`` uses a built-in
default pointing at::

    s3://aind-open-data/HCR_831990-s2-ls1_..._01-32-11/fusion/ch_488/fused.zarr/5

(level 5). Mask parameters can be customized via ``--params-json`` (see
``GfpMaskModel.from_json``); omit it to use defaults. That same JSON may also carry
pipeline-normalization keys ``normalize``/``norm_lower``/``norm_upper`` — set
``"normalize": "global"`` with the dataset's global min/max to skip the per-block
percentile computation that bottlenecks prep, e.g.::

    {"normalize": "global", "norm_lower": 90.0, "norm_upper": 1200.0,
     "threshold": 0.1, "smooth_sigma": [0.5, 1, 1], "open_iterations": 1}

``threshold`` is in normalized ``[0, 1]`` (raw-intensity equivalent is
``norm_lower + threshold*(norm_upper - norm_lower)``); lower => more oversegmentation.
``open_iterations`` is a binary opening that removes tiny dots after thresholding
(``0`` disables it; raise ``threshold`` and/or ``open_iterations`` to despeckle more).

To fix chunk-to-chunk segmentation differences caused by source intensity
inhomogeneity (light-sheet shading / fusion seams), enable white top-hat flat-field
correction. A global background is estimated once from a coarse pyramid level and
subtracted before normalization, so a single global threshold segments uniformly
(seam-free). Add, e.g.::

    {"normalize": "global", "norm_lower": 90.0, "norm_upper": 1200.0,
     "threshold": 0.05, "smooth_sigma": [0.5, 1, 1], "open_iterations": 1,
     "flatfield": true, "flatfield_level": 8, "flatfield_opening_radius": 2,
     "flatfield_sigma": 1.0}

With ``flatfield`` on, ``threshold`` applies to the *flattened* signal and must be
re-tuned (subtraction compresses the dynamic range, so start lower). ``flatfield_level``
is the coarse level the background is estimated from; ``flatfield_opening_radius``
(coarse voxels) strips sparse cells from the estimate; ``flatfield_mode`` may be
``"subtract"`` (default, white top-hat) or ``"divide"`` (multiplicative shading).

For *per-tile* flooding at full-volume scale (whole fused tiles segmented), use
``normalize="adaptive"`` instead: the model is fed a per-voxel local z-score
``(img - B) / S`` (B/S estimated once at a coarse level, sampled per-block, seam-free),
so the threshold adapts per tile. ``threshold`` is then a z-score ``k`` (~2-4). Add::

    {"normalize": "adaptive", "adaptive_level": 6, "adaptive_radius": 8,
     "adaptive_bg_pct": 25.0, "adaptive_hi_pct": 95.0,
     "threshold": 3.0, "smooth_sigma": [0.5, 1, 1], "open_iterations": 1}

``adaptive_radius`` (coarse voxels) is the local window; keep it smaller than a fusion
tile but larger than a neuron cross-section. ``adaptive_*`` subsumes flat-field.

Performance
-----------
The gfp-mask "model" is a classical cupy routine, not a deep net. Its
labeling/morphology steps stall the GPU on host syncs, so GPU *utilization* reads low
even when the GPU stage is the bottleneck — judge by wall-clock and the bottleneck
verdict logged from ``--metrics-json``, not by ``nvidia-smi`` utilization.

Tips:
- Don't over-enlarge ``--patch``: huge patches take longer for the prep workers to
  build and leave fewer in flight, which *starves* the GPU between patches and lowers
  throughput. Keep patch moderate (e.g. 64-128 per axis).
- Biggest win: run several GPU workers per device to overlap one worker's host-sync
  stalls with another's compute, via ``--workers-per-device 3`` (try this first). Costs
  ~N x GPU memory.
"""

